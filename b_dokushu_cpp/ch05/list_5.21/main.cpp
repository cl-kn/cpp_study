#include <iostream>
#include <string>

int main(void)
{
    int &&i = 0;
    int &&j = 0; //0からもう一つ、右辺値参照を作る

    std::cout << "&i " << &i << std::endl; //0x61ff00
    std::cout << "&j " << &j << std::endl; //0x61ff04

    return 0;
}